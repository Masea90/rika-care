// Language utility functions
const i18n = {
  currentLanguage: 'en',
  
  setLanguage(lang) {
    if (languages[lang]) {
      this.currentLanguage = lang;
      localStorage.setItem('rika-language', lang);
    }
  },
  
  getLanguage() {
    return localStorage.getItem('rika-language') || 'en';
  },
  
  t(key) {
    return languages[this.currentLanguage][key] || languages.en[key] || key;
  },
  
  init() {
    this.currentLanguage = this.getLanguage();
  }
};

if (typeof module !== 'undefined' && module.exports) {
  module.exports = { languages, i18n };
}